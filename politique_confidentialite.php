    <h1>Politique de confidentialité</h1>

    <p>Chez Détour, nous accordons une grande importance à la confidentialité et à la sécurité des informations personnelles de nos utilisateurs. Cette politique de confidentialité explique comment nous recueillons, utilisons, divulguons et protégeons les données personnelles que nous collectons auprès de vous lorsque vous utilisez nos services.</p>

    <h2>1. Collecte des informations personnelles</h2>

    <p>Nous pouvons collecter certaines informations personnelles vous concernant lorsque vous utilisez nos services, telles que votre nom, votre adresse e-mail, votre adresse postale et d'autres informations nécessaires pour vous identifier. Nous ne collectons ces informations que si vous nous les fournissez volontairement, par exemple en remplissant des formulaires d'inscription ou en nous contactant directement.</p>

    <h2>2. Utilisation des informations personnelles</h2>

    <p>Nous utilisons les informations personnelles que nous collectons dans le but de vous fournir nos services, de répondre à vos demandes, de personnaliser votre expérience utilisateur et de vous informer sur les mises à jour, les offres spéciales et les nouvelles fonctionnalités de nos produits. Nous pouvons également utiliser vos informations personnelles à des fins statistiques ou de recherche, tout en veillant à ce qu'elles restent anonymes.</p>

    <h2>3. Divulgation des informations personnelles</h2>

    <p>Nous ne vendons, ne louons, ni ne divulguons vos informations personnelles à des tiers, sauf dans les cas suivants :</p>

    <ul>
        <li>Avec votre consentement préalable pour partager des informations spécifiques.</li>
        <li>Si cela est exigé par la loi, une réglementation ou une ordonnance judiciaire.</li>
        <li>Pour protéger nos droits, notre sécurité ou celle des autres utilisateurs.</li>
        <li>Dans le cadre d'une fusion, d'une acquisition ou d'une vente d'actifs, auquel cas nous veillerons à la confidentialité des informations personnelles.</li>
    </ul>

    <h2>4. Sécurité des informations personnelles</h2>

    <p>Nous mettons en place des mesures de sécurité appropriées pour protéger vos informations personnelles contre toute utilisation abusive, perte, accès non autorisé, divulgation ou modification. Nous utilisons des protocoles de cryptage, des pare-feu et des contrôles d'accès pour garantir la confidentialité de vos données.</p>

    <h2>5. Cookies et technologies similaires</h2>

    <p>Nous utilisons des cookies et d'autres technologies similaires pour améliorer votre expérience utilisateur, analyser les tendances, administrer le site web et collecter des informations démographiques sur nos utilisateurs. Vous pouvez choisir de désactiver les cookies dans les paramètres de votre navigateur, mais cela pourrait limiter certaines fonctionnalités de nos services.</p>

    <h2>6. Modifications de la politique de confidentialité</h2>

    <p>Nous nous réservons le droit de mettre à jour cette politique de confidentialité à tout moment. Toute modification sera publiée sur notre site web avec la date d'entrée en vigueur. Nous vous encourageons à consulter régulièrement cette politique pour rester informé des pratiques en matière de confidentialité.</p>

